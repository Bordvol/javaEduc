public class Employee {
    long id;
    String firstName;
    String lastName;
    int age;
    int salary;

    Employee(long newId, String newFirstName, String newLastName, int newAge, int newSalary){
        id = newId;
        firstName = newFirstName;
        lastName = newLastName;
        age = newAge;
        salary = newSalary;
    }


}
